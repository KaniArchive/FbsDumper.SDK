using System.Collections.Immutable;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace FbsDumper.SDK.Generator;

[Generator(LanguageNames.CSharp)]
public sealed class ExtensionRootGenerator : IIncrementalGenerator
{
    private const string AttributeFqn = "FbsDumper.SDK.FbsDumperExtensionAttribute";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        var types = context.SyntaxProvider
            .ForAttributeWithMetadataName(
                AttributeFqn,
                predicate: static (node, _) => node is ClassDeclarationSyntax,
                transform: static (ctx, _) => (INamedTypeSymbol)ctx.TargetSymbol)
            .Collect();

        context.RegisterSourceOutput(types, Emit);
    }

    private static void Emit(SourceProductionContext ctx, ImmutableArray<INamedTypeSymbol> types)
    {
        if (types.IsEmpty) return;

        ctx.AddSource(
            "FbsDumperExtensionRoots.g.cs",
            SourceText.From(BuildSource(types), Encoding.UTF8));
    }

    private static string BuildSource(ImmutableArray<INamedTypeSymbol> types)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// <auto-generated/>");
        sb.AppendLine("// Generated by FbsDumper.SDK — do not edit manually.");
        sb.AppendLine("#pragma warning disable");
        sb.AppendLine("using System.Diagnostics.CodeAnalysis;");
        sb.AppendLine();
        sb.AppendLine("namespace FbsDumper.SDK.Generated");
        sb.AppendLine("{");
        sb.AppendLine("    internal static class ExtensionRoots");
        sb.AppendLine("    {");

        foreach (var type in types)
        {
            var typeName = type.ToDisplayString();
            var asmName  = type.ContainingAssembly.Name;

            sb.AppendLine($"        // {typeName}");
            sb.AppendLine($"        [DynamicDependency(");
            sb.AppendLine($"            DynamicallyAccessedMemberTypes.All,");
            sb.AppendLine($"            \"{typeName}\",");
            sb.AppendLine($"            \"{asmName}\")]");
        }

        sb.AppendLine("        internal static void KeepRoots() { }");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }
}
